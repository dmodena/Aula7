    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(validarEntrada()) {
            aluno = new Aluno(Integer.parseInt(tfMatricula.getText().trim()), 
                    tfNome.getText().trim(),
                    tfEmail.getText().trim());
            aluno.setNota1(Double.parseDouble(tfNota1.getText().trim()));
            aluno.setNota2(Double.parseDouble(tfNota2.getText().trim()));
            
            frameAluno.adicionarAluno(aluno);
            this.dispose();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private boolean validarEntrada() {
        boolean valido = false;
        
        if(!tfMatricula.getText().trim().isEmpty() ||
        tfNome.getText().trim().isEmpty() ||
        tfEmail.getText().trim().isEmpty() ||
        tfNota1.getText().trim().isEmpty() ||
        tfNota2.getText().trim().isEmpty()) {
            try {
                int matricula = Integer.parseInt(tfMatricula.getText().trim());
                double nota1 = Double.parseDouble(tfNota1.getText().trim());
                double nota2 = Double.parseDouble(tfNota2.getText().trim());
                
                valido = true;
            }
            catch(Exception e) {
                valido = false;
            }
        }
        
        return valido;
    }
    
